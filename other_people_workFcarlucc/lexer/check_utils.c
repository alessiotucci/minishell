#include "../minishell.h"

int	is_op(char *c)
{
	if (c[0] == '|' && c[1] == 0)
		return (-1);
	if (((c[0] == '|' && c[1] == '|') || (c[0] == '&' && c[1] == '&')) \
	&& c[2] == 0)
		return (-1);
	if ((c[0] == '|' && c[1] == '|') || (c[0] == '&' && c[1] == '&') \
	|| (c[0] == '|' && c[1] == ' '))
		return (1);
	return (0);
}

int	is_red(char *c)
{
	if ((c[0] == '<' && c[1] == 0) || (c[0] == '>' && c[1] == 0))
		return (-1);
	if (((c[0] == '<' && c[1] == '<') || (c[0] == '>' && c[1] == '>')) \
	&& c[2] == 0)
		return (-1);
	if ((c[0] == '<' && c[1] == '<') || (c[0] == '>' && c[1] == '>') \
	|| (c[0] == '<' && c[1] == ' ') || (c[0] == '>' && c[1] == ' '))
		return (1);
	return (0);
}

int	is_one_char(char *c)
{
	if ((c[0] == '|' && c[1] == ' ') || (c[0] == '&' && c[1] == ' ') \
	|| (c[0] == '<' && c[1] == ' ') || (c[0] == '>' && c[1] == ' '))
		return (1);
	return (0);
}

int	is_two_char(char *c)
{
	if ((c[0] == '|' && c[1] == '|') || (c[0] == '&' && c[1] == '&') \
	|| (c[0] == '<' && c[1] == '<') || (c[0] == '>' && c[1] == '>'))
		return (1);
	return (0);
}

